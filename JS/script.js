function getPageContent(page) {
    var contentToReturn;

    switch (page) {
        case 'Dashboard':
            contentToReturn = pages.Dashboard;
            break;
        case 'Plan':
            contentToReturn = pages.Plan;
            break;
        case 'Account':
            contentToReturn = pages.Account;
            break;
        case 'Payments':
            contentToReturn = pages.Payments;
            break;
        case 'API':
            contentToReturn = pages.API;
            break;
        case 'Affiliate':
            contentToReturn = pages.Affiliate;
            break;

        default:
            contentToReturn = pages.Dashboard;
            break;
    }
    document.getElementById('content').innerHTML = contentToReturn;
}
let sidebar = document.getElementById('sidebar');
let text = document.querySelectorAll('.text');
let content = document.getElementById('content');
sidebar.addEventListener('mouseenter', e => {
    sidebar.style.width = '18%';
    content.style.width = '82%';
    sidebar.style.transition = 'width 0.3s ease-in-out';
    content.style.transition = 'width 0.3s ease-in-out';
    text.forEach(element => {
        element.style.display = 'inline-block';
    });
});

sidebar.addEventListener('mouseleave', e => {
    sidebar.style.width = '6%';
    content.style.width = '94%';
    sidebar.style.transition = 'width 1s ease-in-out';
    content.style.transition = 'width 1s ease-in-out';
    text.forEach(element => {
        element.style.display = 'none';
    });


});
